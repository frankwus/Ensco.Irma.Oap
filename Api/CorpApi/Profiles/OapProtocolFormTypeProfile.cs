using AutoMapper;
using Ensco.Irma.Models.Domain.Oap.Checklist;

namespace Ensco.Irma.RigApi.Profiles
{
    public class OapProtocolFormTypeProfile : Profile
    {
        public OapProtocolFormTypeProfile()
        {
            CreateMap<OapProtocolFormType, OapProtocolFormType>()
                .ForMember(d => d.CreatedDateTime, opt => opt.Ignore())
                .ForMember(d => d.CreatedBy, opt => opt.Ignore());
        }
    }
}